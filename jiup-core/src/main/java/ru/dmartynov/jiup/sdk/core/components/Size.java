public enum Size {
    /**
     * Defines the dialog’s width (or height) equal to the screen's width (or height)
     */
    FULL,
    /**
     * Defines the dialog’s width (or height) equal to half the screen's width (or height)
     */
    HALF,
    /**
     * Defines the dialog’s width (or height) equal to 1/3 the screen's width (or height)
     */
    THIRD,
    /**
     * Defines the dialog’s width (or height) equal to 1/4 of the screen's width (or height)
     */
    QUARTER,
    /**
     * Defines the dialog’s width (or height) equal to 1/8 of the screen's width (or height)
     */
    EIGHTH
}
